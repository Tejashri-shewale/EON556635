package Strings;

public class SubString {

	public static void main(String[] args) 
	{
		String s="Tejashri Shewale";
		System.out.println("original String "+s);
		System.out.println("substring index 6"+s.substring(6));
		System.out.println("substring index 0 to6 "+s.substring(0,6));

	}

}
