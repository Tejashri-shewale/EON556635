package operators;

public class assignment {

	public static void main(String[] args) 
	{
		int x=12,y=13,z=16;
		System.out.println("\n assign value is : ");
		x+=2;
		y-=2;
		z*=2;
		System.out.println("\n value of x : "+x);
		System.out.println("\n value of y : "+y);
		System.out.println("\n value of z : "+z);
	}

}
