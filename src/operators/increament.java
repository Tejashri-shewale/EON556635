package operators;

public class increament {

	public static void main(String[] args) {
		int a=10,b=5;
		System.out.println("\n value of a "+a);
		System.out.println("\n value of b "+b);
		
		System.out.println("value of ++a "+(++a));
		System.out.println("value of a "+a);
		System.out.println("value of b++ "+(b++));
		System.out.println("value of b "+b);
		

	}

}
